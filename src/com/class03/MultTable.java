package com.class03;

public class MultTable {

	public static void main(String[] args) {
		for(int i=1;i<10;i++) {
			for(int j=1;j<10;j++) {
				int result=i*j;
				System.out.print(i+"*"+j+"="+result);
				
				
		}

	}
		for(int a=0;a<=6;a++) {
			
			for(int b=1;b<a;b++) {
				
				System.out.print("* ");
				
			}
			System.out.println();
		}

}}
